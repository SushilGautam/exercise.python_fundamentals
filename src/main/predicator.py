class Predicator(object):
    def is_greater_than_5(self, a):
        if a > 5:
            return True
        else:
            return False

    def is_greater_than_8(self, b):
        if b > 8:
            return True
        else:
            return False

    def is_less_than_4(self, c):
        if c < 4:
            return True
        else:
            return False


    def is_less_than_1(self, d):
        if d < 1:
            return True
        else:
            return False